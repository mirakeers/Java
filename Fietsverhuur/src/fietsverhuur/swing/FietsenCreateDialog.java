    public Fiets getFiets() {
        Fiets resultaat;
        
        String gender = cmbGender.getModel().getSelectedItem().toString();
        String fietstype = cmbFietstype.getModel().getSelectedItem().toString();
        int aantalVitessen;
        if(!txtAantalVitessen.getText().equals("")) {
            aantalVitessen = Integer.parseInt(txtAantalVitessen.getText());
        } else {
            aantalVitessen = 0;
        }
        String dikteBand = cmbDikteBand.getModel().getSelectedItem().toString();
        if(dikteBand.equals("Kies dikte...")) {
            dikteBand = "";
        }
        String kleur = txtKleur.getText();
        int verhuurpuntId = Integer.parseInt(txtVerhuurpuntId.getText());

        resultaat = new Fiets(gender, fietstype, aantalVitessen, dikteBand, kleur, verhuurpuntId);
        return resultaat;
    }
