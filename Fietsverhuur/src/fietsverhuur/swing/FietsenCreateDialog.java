    private void cmbFietstypeItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbFietstypeItemStateChanged
        if(cmbFietstype.getModel().getSelectedItem().equals("stadsfiets")) {
            txtAantalVitessen.setEnabled(false);
            cmbDikteBand.setEnabled(false);
            txtKleur.setEnabled(true);
        } else {
            txtAantalVitessen.setEnabled(true);
            cmbDikteBand.setEnabled(true);
            txtKleur.setEnabled(false);
        }
    }//GEN-LAST:event_cmbFietstypeItemStateChanged

    public void enableDisableOpslaanButton() {
        if(txtVerhuurpuntId.getText().equals("")) {
            btnSaveDataAndCloseGUI.setEnabled(false);
            lblFoutmelding.setText("Vul a.u.b. alle verplichte velden in.");
        } else {
            if(CheckUserInput.doesVerhuurpuntIdExist(txtVerhuurpuntId.getText(), lblFoutmelding)){
                if(txtAantalVitessen.getText().equals("")|| CheckUserInput.isStringInteger(txtAantalVitessen.getText())) {
                    btnSaveDataAndCloseGUI.setEnabled(true);
                    lblFoutmelding.setText("");
                } else {
                    btnSaveDataAndCloseGUI.setEnabled(false);
                    lblFoutmelding.setText("Vul a.u.b. een geldig aantal vitessen in, of laat het tekstveld leeg.");
                }
            } else {
                btnSaveDataAndCloseGUI.setEnabled(false);
            }
        }
    }
    
    
    
    
    
