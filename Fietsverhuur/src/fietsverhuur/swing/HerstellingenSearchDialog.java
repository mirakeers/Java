    private void btnSearchHerstellingenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchHerstellingenActionPerformed
        herstellingenModel.clear();
        if(txtHerstellingId.isEnabled()) {
            int herstellingid = Integer.parseInt(txtHerstellingId.getText());
            Herstelling gevondenHerstelling = HerstellingDao.getHerstellingById(herstellingid);
            herstellingenModel.addElement(gevondenHerstelling);
        }
        if(txtMeldingsdatum.isEnabled()) {
            try {
                Date meldingsdatum = Calculate.df.parse(txtMeldingsdatum.getText());
                ArrayList<Herstelling> gevondenHerstellingenLijst = HerstellingDao.getHerstellingenByMeldingsdatum(meldingsdatum);
                for(Herstelling gevondenHerstelling : gevondenHerstellingenLijst) {
                    herstellingenModel.addElement(gevondenHerstelling);
                }
                
            } catch (ParseException ex) {
                lblFoutmelding.setText("Gelieve een geldige datum in te voeren.");
            }
        }
        if(txtHuurderId.isEnabled()) {
            int huurderId = Integer.parseInt(txtHuurderId.getText());
            ArrayList<Herstelling> gevondenHerstellingenLijst = HerstellingDao.getHerstellingenByHuurderId(huurderId);
            for(Herstelling gevondenHerstelling : gevondenHerstellingenLijst) {
                herstellingenModel.addElement(gevondenHerstelling);
            }
        }
        if(txtFietsnummer.isEnabled()) {
            int fietsnummer = Integer.parseInt(txtFietsnummer.getText());
            ArrayList<Herstelling> gevondenHerstellingenLijst = HerstellingDao.getHerstellingenByFietsnummer(fietsnummer);
            for(Herstelling gevondenHerstelling : gevondenHerstellingenLijst) {
                herstellingenModel.addElement(gevondenHerstelling);
            }
        }
        if(txtDefectnummer.isEnabled()) {
            int defectnummer = Integer.parseInt(txtDefectnummer.getText());
            ArrayList<Deelherstelling> gevondenDeelherstellingenLijst = DeelherstellingDao.getDeelherstellingenByDefectnummer(defectnummer);
            for(Deelherstelling gevondenDeelherstelling : gevondenDeelherstellingenLijst) {
                int herstellingId = gevondenDeelherstelling.getHerstellingId();
                Herstelling gevondenHerstelling = HerstellingDao.getHerstellingById(herstellingId);
                herstellingenModel.addElement(gevondenHerstelling);
            }
        }
        if(txtKostprijsMin.isEnabled()) {
            double kostprijsMin = 0;
            double kostprijsMax = 99999;
            if(!txtKostprijsMin.getText().equals("")) {
                kostprijsMin = Integer.parseInt(txtKostprijsMin.getText());
            }
            if(!txtKostprijsMax.getText().equals("")) {
                kostprijsMax = Integer.parseInt(txtKostprijsMax.getText());
            }
            ArrayList<Herstelling> herstellingenlist = HerstellingDao.getHerstellingen();
            for(Herstelling currentHerstelling : herstellingenlist) {
                ArrayList<Integer> defectnummers = new ArrayList<Integer>();
                ArrayList<Deelherstelling> gevondenDeelherstellingen = DeelherstellingDao.getDeelherstellingenByHerstellingId(currentHerstelling.getHerstellingId());
                for(Deelherstelling currentDeelherstelling : gevondenDeelherstellingen) {
                    defectnummers.add(currentDeelherstelling.getDefectnummer());
                }
                double kostprijs = Calculate.calculateHerstellingKostprijs(defectnummers);
                if(kostprijsMin <= kostprijs && kostprijsMax > kostprijs) {
                    herstellingenModel.addElement(currentHerstelling);
                }
            }
            
        }
    }//GEN-LAST:event_btnSearchHerstellingenActionPerformed

