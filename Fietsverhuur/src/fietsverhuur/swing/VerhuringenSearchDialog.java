    private void btnSearchVerhuringenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchVerhuringenActionPerformed
        verhuringenModel.clear();
        if(txtVerhuringId.isEnabled()) {
            int verhuringid = Integer.parseInt(txtVerhuringId.getText());
            Verhuring gevondenVerhuring = VerhuringDao.getVerhuringById(verhuringid);
            verhuringenModel.addElement(gevondenVerhuring);
        }
        if(txtBegindatum.isEnabled()) {
            try {
                Date begindatum = Calculate.df.parse(txtBegindatum.getText());
                ArrayList<Verhuring> gevondenVerhuringenLijst = VerhuringDao.getVerhuringenByBegindatum(begindatum);
                for(Verhuring gevondenVerhuring : gevondenVerhuringenLijst) {
                    verhuringenModel.addElement(gevondenVerhuring);
                }
                
            } catch (ParseException ex) {
                lblFoutmelding.setText("Gelieve een geldige datum in te voeren.");
            }
        }
        if(txtDuurMin.isEnabled()) {
            int duurMin = 0;
            int duurMax = 366;
            if(!txtDuurMin.getText().equals("")) {
                duurMin = Integer.parseInt(txtDuurMin.getText());
            }
            if(!txtDuurMax.getText().equals("")) {
                duurMax = Integer.parseInt(txtDuurMax.getText());
            }
            ArrayList<Verhuring> gevondenVerhuringenLijst = VerhuringDao.getVerhuringenByDuurInDagen(duurMin, duurMax);
            for(Verhuring gevondenVerhuring : gevondenVerhuringenLijst) {
                verhuringenModel.addElement(gevondenVerhuring);
            }
        }
        if(txtEinddatum.isEnabled()) {
            try {
                Date einddatum = Calculate.df.parse(txtEinddatum.getText());
                ArrayList<Verhuring> gevondenVerhuringenLijst = VerhuringDao.getVerhuringenByEinddatum(einddatum);
                for(Verhuring gevondenVerhuring : gevondenVerhuringenLijst) {
                    verhuringenModel.addElement(gevondenVerhuring);
                }
                
            } catch (ParseException ex) {
                lblFoutmelding.setText("Gelieve een geldige datum in te voeren.");
            }
        }
        if(txtHuurderId.isEnabled()) {
            int huurderId = Integer.parseInt(txtHuurderId.getText());
            ArrayList<Verhuring> gevondenVerhuringenLijst = VerhuringDao.getVerhuringenByHuurderId(huurderId);
            for(Verhuring gevondenVerhuring : gevondenVerhuringenLijst) {
                verhuringenModel.addElement(gevondenVerhuring);
            }
        }
        if(txtFietsnummer.isEnabled()) {
            int fietsnummer = Integer.parseInt(txtFietsnummer.getText());
            ArrayList<Verhuring> gevondenVerhuringenLijst = VerhuringDao.getVerhuringenByFietsnummer(fietsnummer);
            for(Verhuring gevondenVerhuring : gevondenVerhuringenLijst) {
                verhuringenModel.addElement(gevondenVerhuring);
            }
        }
        if(txtKostprijsMin.isEnabled()) {
            double kostprijsMin = 0;
            double kostprijsMax = 99999;
            if(!txtKostprijsMin.getText().equals("")) {
                kostprijsMin = Integer.parseInt(txtKostprijsMin.getText());
            }
            if(!txtKostprijsMax.getText().equals("")) {
                kostprijsMax = Integer.parseInt(txtKostprijsMax.getText());
            }
            ArrayList<KostprijsPerVerhuringId> gevondenKostprijsPerVerhuringIdLijst = KostprijsPerVerhuringIdDao.getKostprijzenPerVerhuringIdByKostprijs(kostprijsMin, kostprijsMax);
            for(KostprijsPerVerhuringId gevondenKostprijsPerVerhuringId : gevondenKostprijsPerVerhuringIdLijst) {
                Verhuring gevondenVerhuring = VerhuringDao.getVerhuringById(gevondenKostprijsPerVerhuringId.getVerhuringId());
                verhuringenModel.addElement(gevondenVerhuring);
            }
        }
    }//GEN-LAST:event_btnSearchVerhuringenActionPerformed

